#include <iostream>
#include <string>

using namespace std;

const char STOP[] = "done";

int main()
{
    string words;
    int cnt = 0;

    cout << "Enter words (to stop, type the word done): ";

    do
    {
        cin >> words;
        cin.get(); //cin读取时不会将空白字符从输入队列中删除
        cnt ++;
    } while (words != STOP); //直接使用关系运算符进行字符串的比较


    cout << "You enter a total of " << cnt - 1 << " words." << endl;

    return 0;
}